package modelo;

public class Punto {
	private int x;
	private int y;
	private String color;
	
	public Punto() {}
	
	public int getX() {
		return x;
	}
	
	public void setX(int s) {
		this.x = s;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int s) {
		this.y = s;
	}
	
	public String getColor() {
		return color;
	}
	
	public void setColor(String s) {
		this.color = s;
	}
}
